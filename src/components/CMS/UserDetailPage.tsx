      const tempUser = { 
        userId, 
        displayName: user?.display_name || `User_${userId.slice(0, 8)}` 
      };
      const now = new Date();
      const data: ChartDataPoint[] = [];

      console.log('üìä „ÉÅ„É£„Éº„Éà„Éá„Éº„ÇøÂèñÂæóÈñãÂßã:', { tempUser, period: selectedPeriod });

