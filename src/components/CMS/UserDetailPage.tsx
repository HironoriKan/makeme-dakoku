      setMaxSales(max);
      setAvgSales(avg);

      // „Éá„Éº„Çø„ÅåÊõ¥Êñ∞„Åï„Çå„Åü„ÇâÊúÄÊñ∞Êó•‰ªò„Å´„Çπ„ÇØ„É≠„Éº„É´
